/**
 * Similar methods for integer encoding according to HPACK specification.
 * For encode, also consumes the prefix and mask for the flag encoding in the
 * start byte, returns the number of bytes used in the encoding.  Destination
 * block should have at least 6 bytes available.
 */
size_t WXHTTP_HEncodeInt(uint32_t val, uint8_t prefix, uint8_t mask,
                         uint8_t *dst) {
    uint8_t *ptr = dst;

    /* If the value fits, wear it! */
    if (val < mask) {
        *ptr = prefix | (val & mask);
        return 1;
    }

    /* This really messed me up for a bit (the subtraction) */
    *(ptr++) = prefix | mask;
    val -= mask;

    /* 7-bit encoding of the remaining bits */
    while (val >= 128) {
        *(ptr++) = (val & 0x7f) | 0x80;
        val >>= 7;
    }
    *(ptr++) = val & 0x7f;

    return ptr - dst;
}

/**
 * For decode, caller is responsible for extracting prefix bits prior to call.
 * Returns number of bytes read from source, DATA_ERROR for invalid encoding
 * or TRUNCATED if truncated (not OK in this instance).
 */
ssize_t WXHTTP_HDecodeInt(uint8_t *src, size_t len, uint8_t mask,
                          uint32_t *val) {
    uint64_t accumulator = 0;
    uint32_t ch;
    int idx;

    /* Easy case if mask is not full */
    if (len == 0) return WXNRC_TIMEOUT;
    *val = *(src++) & mask;
    if (*val != mask) return 1;

    /* Scan for the remainder */
    for (idx = 0; (idx < len) && (idx < 6); idx++) {
        ch = *(src++);
        accumulator += ((uint64_t) (ch & 0x7f)) << (7 * idx);
        if ((ch & 0x80) == 0) {
            if (accumulator > UINT32_MAX) return WXNRC_DATA_ERROR;
            *rval = (uint32_t) accumulator;
            return idx + 2;
        }
    }

    return (idx < 6) ? WXNRC_TRUNCATED : WXNRC_DATA_ERROR;
}

/* For order of development (and dynamics management), header processing here */
static WXHTTPHeader *_allocInitHeader(WXHTTPHeader *trg, uint8_t *name,
                                      uint32_t nameLen, uint8_t *value,
                                      uint32_t valueLen, uint32_t flags) {
    WXHTTPHeader *retval = trg;

    /* Method supports both allocation and infill */
    if (trg == NULL) {
        retval = (WXHTTPHeader *) WXCalloc(sizeof(WXHTTPHeader));
        if (retval == NULL) return NULL;
    }

    retval->nameLen = nameLen;
    if ((flags & (WXHTTP_HDR_STATIC_NAME | WXHTTP_HDR_PREALLOC_NAME)) != 0) {
        retval->name = name;
    } else {
        if (retval->nameLen < 31) {
            retval->name = retval->nameBuff;
        } else {
            retval->name = WXMalloc(nameLen + 1);
            if (retval->name == NULL) {
                if (trg == NULL) WXFree(retval);
                return NULL;
            }
        }
        (void) memcpy(retval->name, name, nameLen);
        retval->name[nameLen] = '\0';
    }
    retval->nameHash = WXHash_StrCaseHashFn(retval->name);

    retval->valueLen = valueLen;
    if ((flags & (WXHTTP_HDR_STATIC_VALUE | WXHTTP_HDR_PREALLOC_VALUE)) != 0) {
        retval->value = value;
    } else {
        retval->value = WXMalloc(valueLen + 1);
        if (retval->value == NULL) {
            if (((flags & WXHTTP_HDR_STATIC_NAME) == 0) &&
                    (retval->name != retval->nameBuff)) WXFree(retval->name);
            if (trg == NULL) WXFree(retval);
            return NULL;
        }
        (void) memcpy(retval->value, value, valueLen);
        retval->value[valueLen] = '\0';
    }
    retval->valueHash = WXHash_StrHashFn(retval->value);

    /* Remove the prealloc flags */
    retval->flags = flags & 0x3f;

    return retval;
}
WXHTTPHeader *WXHTTP_AllocInitHeader(WXHTTPHeader *trg, uint8_t *name,
                                     uint8_t *val, uint32_t flags) {
    return _allocInitHeader(trg, name, strlen(name), val, strlen(val), flags);
}

WXHTTPHeader *WXHTTP_CloneHeader(WXHTTPHeader *trg, WXHTTPHeader *src) {
    WXHTTPHeader *retval = trg;

    /* Like allocate, method supports both allocation and infill */
    if (trg == NULL) {
        retval = (WXHTTPHeader *) WXCalloc(sizeof(WXHTTPHeader));
        if (retval == NULL) return NULL;
    }

    /* Copy lots of data in a single blow */
    (void) memcpy(retval, src, sizeof(WXHTTPHeader));

    if ((retval->flags & WXHTTP_HDR_STATIC_NAME) == 0) {
        if (retval->nameLen < 31) {
            retval->name = retval->nameBuff;
            if (src->name != src->nameBuff) {
                /* Original was allocated but here we can use local */
                (void) strcpy(retval->name, src->name);
            }
        } else {
            retval->name = WXMalloc(retval->nameLen + 1);
            if (retval->name == NULL) {
                if (trg == NULL) WXFree(retval);
                return NULL;
            }
            (void) strcpy(retval->name, src->name);
        }
    }

    if ((retval->flags & WXHTTP_HDR_STATIC_VALUE) == 0) {
        retval->value = WXMalloc(retval->valueLen + 1);
        if (retval->value == NULL) {
            if (((retval->flags & WXHTTP_HDR_STATIC_NAME) == 0) &&
                    (retval->name != retval->nameBuff)) WXFree(retval->name);
            if (trg == NULL) WXFree(retval);
            return NULL;
        }
        (void) strcpy(retval->value, src->value);
    }

    return retval;
}

void WXHTTP_DestroyHeader(WXHTTPHeader *hdr, int incInstance) {
    if (((hdr->flags & WXHTTP_HDR_STATIC_NAME) == 0) &&
            (hdr->name != hdr->nameBuff)) WXFree(hdr->name);
    if ((hdr->flags & WXHTTP_HDR_STATIC_VALUE) == 0) WXFree(hdr->value);
    if (incInstance) WXFree(hdr);
}
void WXHTTP_DestroyHeaderChain(WXHTTPHeader *hdr) {
    WXHTTPHeader *next;

    while (hdr != NULL) {
        next = hdr->next;
        WXHTTP_DestroyHeader(hdr, TRUE);
        hdr = next;
    }
}

/* Split into a separate method for clarity and dual use */
static int _pushDynamicHeader(WXHTTPDynamicHeaders *dynTable,
                              WXHTTPHeader *hdr) {
    uint32_t entryLen = hdr->nameLen + hdr->valueLen + 32, allocCount;
    WXHTTPHeader *entry, **newEntries;
    int munch = 0;

    /* Purge from existing to manage dynamic table cap (oldest are first) */
    while ((dynTable->tableSize > 0) &&
               ((dynTable->tableSize + entryLen) > dynTable->maxTableSize)) {
        entry = dynTable->entries[munch];
        dynTable->tableSize -= entry->nameLen + entry->valueLen + 32;
        WXHTTP_DestroyHeader(entry, TRUE);
        munch++; dynTable->entryCount--;
    }
    if ((dynTable->entryCount != 0) && (munch != 0)) {
        (void) memmove(dynTable->entries, dynTable->entries + munch,
                       dynTable->entryCount * sizeof(void *));
    }

    /* Special overflow case, empties but does not cache */
    if (entryLen > dynTable->maxTableSize) return WXNRC_OK;

    /* Add the newest cache entry, resizing as needed */
    if (dynTable->entryCount >= dynTable->entryAllocCount) {
        if (dynTable->entryAllocCount == 0) {
            newEntries = (WXHTTPHeader **) WXMalloc(16 * sizeof(void *));
            if (newEntries == NULL) return WXNRC_MEM_ERROR;
            dynTable->entries = newEntries;
            dynTable->entryAllocCount = 16;
        } else {
            /* TODO - seem to like the doubling algorithm */
            allocCount = dynTable->entryAllocCount << 1;
            newEntries = (WXHTTPHeader **) WXRealloc(dynTable->entries,
                                                  allocCount * sizeof(void *));
            if (newEntries == NULL) return WXNRC_MEM_ERROR;
            dynTable->entries = newEntries;
            dynTable->entryAllocCount = allocCount;
        }
    }
    entry = WXHTTP_CloneHeader(NULL, hdr);
    if (entry == NULL) return WXNRC_MEM_ERROR;
    dynTable->entries[dynTable->entryCount++] = entry;
    dynTable->tableSize += entryLen;
    return WXNRC_OK;
}

static void _resizeDynamicHeaders(WXHTTPDynamicHeaders *dynTable,
                                  uint32_t newSize) {
    WXHTTPHeader *entry;
    int munch = 0;

    /* Purge from existing to fit new size (oldest are first) */
    while (dynTable->tableSize > newSize) {
        entry = dynTable->entries[munch];
        dynTable->tableSize -= entry->nameLen + entry->valueLen + 32;
        WXHTTP_DestroyHeader(entry, TRUE);
        munch++; dynTable->entryCount--;
    }
    if ((dynTable->entryCount != 0) && (munch != 0)) {
        (void) memmove(dynTable->entries, dynTable->entries + munch,
                       dynTable->entryCount * sizeof(void *));
    }

    dynTable->maxTableSize = newSize;
}

/* Handle Huffman string literal encode, destination should be 6 + len bytes */
size_t _encodeStringLiteral(uint8_t *dst, uint8_t *val, uint32_t len,
                           int isLower) {
    uint8_t enc[16], *ptr = dst;
    size_t slen, ilen;
    int idx;

    slen = WXHTTP_HEncodeStr(val, len, ptr, isLower);
    if (slen == 0) {
        /* Unencrypted string instance, direct write content */
        ilen = WXHTTP_HEncodeInt(len, 0x00, 0x7f, ptr);
        ptr += ilen;
        (void) memcpy(ptr, val, len);
        slen = len;
    } else {
        /* Encoded in place, shift to insert length leader with Huffman bit */
        ilen = WXHTTP_HEncodeInt(slen, 0x80, 0x7f, enc);
        (void) memmove(ptr + ilen, ptr, slen);
        (void) memcpy(ptr, enc, ilen);
    }

    return ilen + slen;
}

/* Internal method to handle the HPACK header encoding with dynamic table */
int WXHTTP_HPackHeaders(WXBuffer *buff, WXHTTPHeader *hdrs,
                        WXHTTPDynamicHeaders *dynTable) {
    int rc, idx, dynIdx, indexable;
    uint8_t prefix, mask, *ptr;
    uint32_t nameIdx, fullIdx;
    WXHTTPHeader *entry;
    ssize_t len;

    /* Stream through the headers */
    while (hdrs != NULL) {
        /* Start with the static table */
        indexable = (hdrs->flags & 0x03) == 0;
        nameIdx = fullIdx = 0;
        for (idx = 0; idx < STATIC_HEADER_COUNT; idx++) {
            /* Capture first name match, break on find if not indexable */
            if ((hdrs->nameHash != wxhttpStaticHeaders[idx].nameHash) ||
                    (hdrs->nameLen != wxhttpStaticHeaders[idx].nameLen) ||
                    (memcmp(hdrs->name, wxhttpStaticHeaders[idx].name,
                            hdrs->nameLen) != 0)) continue;
            if (nameIdx == 0) {
                 nameIdx = idx;
                 if (!indexable) break;
            }

            /* Check for value match but only if indexable, break on find */
            if (!indexable) continue;
            if ((hdrs->valueHash != wxhttpStaticHeaders[idx].valueHash) ||
                    (hdrs->valueLen != wxhttpStaticHeaders[idx].valueLen) ||
                    (memcmp(hdrs->value, wxhttpStaticHeaders[idx].value,
                            hdrs->valueLen) != 0)) continue;
            if (fullIdx == 0) fullIdx = idx;
            break;
        }

        /* Only process the dynamic table for non-indexed values (per spec) */
        if ((nameIdx == 0) || (indexable && (fullIdx == 0))) {
            dynIdx = STATIC_HEADER_COUNT;
            for (idx = dynTable->entryCount - 1; idx >= 0; idx--, dynIdx++) {
                /* Capture first name match, break on find if not indexable */
                entry = dynTable->entries[idx];
                if ((hdrs->nameHash != entry->nameHash) ||
                        (hdrs->nameLen != entry->nameLen) ||
                        (memcmp(hdrs->name, entry->name,
                                hdrs->nameLen) != 0)) continue;
                if (nameIdx == 0) {
                     nameIdx = dynIdx;
                     if (!indexable) break;
                }

                /* Check for value match but only if indexable, break on find */
                if (!indexable) continue;
                if ((hdrs->valueHash != entry->valueHash) ||
                        (hdrs->valueLen != entry->valueLen) ||
                        (memcmp(hdrs->value, entry->value,
                                hdrs->valueLen) != 0)) continue;
                if (fullIdx == 0) fullIdx = dynIdx;
                break;
            }
        }

        /* Encode based on matches and indexing requirements */
        if (fullIdx != 0) {
            /* Fully indexed header per Section 6.1, high order bit marked */
            if (WXBuffer_EnsureCapacity(buff, 6,
                                        TRUE) == NULL) return WXNRC_MEM_ERROR;
            len = WXHTTP_HEncodeInt(fullIdx, 0x80, 0x7f,
                                    buff->buffer + buff->length);
            buff->length += len;
        } else {
            /* Only cache it in the dynamic table if indexable */
            if (indexable) {
                rc = _pushDynamicHeader(dynTable, hdrs);
                if (rc != WXNRC_OK) return rc;

                prefix = 0x40;
                mask = 0x3f;
            } else {
                prefix = (hdrs->flags & WXHTTP_HDR_TRANSIENT) ? 0x00 : 0x10;
                mask = 0x0f;
            }

            /* Allocate based on worst case encoding */
            len = 12 + hdrs->valueLen + ((nameIdx == 0) ? hdrs->nameLen : 0);
            if (WXBuffer_EnsureCapacity(buff, len,
                                        TRUE) == NULL) return WXNRC_MEM_ERROR;
            ptr = buff->buffer + buff->length;

            /* Name can be indexed or encoded */
            if (nameIdx == 0) {
                *(ptr++) = prefix;
                ptr += _encodeStringLiteral(ptr, hdrs->name, hdrs->nameLen,
                                            TRUE);
            } else {
                ptr += WXHTTP_HEncodeInt(nameIdx, prefix, mask, ptr);
            }

            /* Value is always encoded */
            ptr += _encodeStringLiteral(ptr, hdrs->value, hdrs->valueLen,
                                        FALSE);
            buff->length = ptr - buff->buffer;
        }

        hdrs = hdrs->next;
    }

    return WXNRC_OK;
}

/* A lot of reversals of the above */
ssize_t _decodeStringLiteral(uint8_t *ptr, uint32_t len,
                             uint8_t **strRef, uint32_t* strLen) {
    ssize_t l, declen;
    uint32_t slen;
    uint8_t *str;

    /* Names and values have same encoding sequence */
    l = WXHTTP_HDecodeInt(ptr, len, 0x7f, &slen);
    if (l < 0) return l;
    if (len < (l + slen)) return WXNRC_TRUNCATED;

    if ((*ptr & 0x80) == 0) {
        str = WXMalloc(slen + 1);
        if (str == NULL) return WXNRC_MEM_ERROR;
        (void) memcpy(str, ptr + l, slen);
        str[slen] = '\0';
        *strLen = slen;
    } else {
        str = WXMalloc(2* slen + 1);
        if (str == NULL) return WXNRC_MEM_ERROR;
        declen = WXHTTP_HDecodeStr(ptr + l, slen, str);
        if (declen < 0) return declen;
        str[declen] = '\0';
        *strLen = declen;
    }
    *strRef = str;

    return l + slen;
}

/* And the other half of the equation, automatically chains to existing */
int WXHTTP_HUnpackHeaders(WXBuffer *buff, WXHTTPHeader **hdrRef,
                          WXHTTPDynamicHeaders *dynTable) {
    uint8_t ch, mask, *ptr = buff->buffer + buff->offset, *nm, *val;
    ssize_t l, len = buff->length - buff->offset;
    int rc, isFullyIndexed, isResize;
    uint32_t idx, nmLen, valLen;
    WXHTTPHeaderOptions flags;
    WXHTTPHeader *hdr;

    /* Read headers until nothing remains or truncation encountered */
    while (len > 0) {
        /* Header encoding based on first byte prefix */
        ch = *ptr;
        isFullyIndexed = isResize = FALSE;
        flags = WXHTTP_HDR_STD;
        if ((ch & 0x80) != 0) {
            /* Fully indexed header */
            isFullyIndexed = TRUE;
            mask = 0x7f;
        } else if ((ch & 0x40) != 0) {
            /* Literal header with incremental idxing */
            mask = 0x3f;
        } else if ((ch & 0x20) != 0) {
            /* Dynamic table size update */
            isResize = TRUE;
            mask = 0x1f;
        } else if ((ch & 0x10) != 0) {
            /* Literal header never indexed */
            flags = WXHTTP_HDR_SECURE;
            mask = 0x0f;
        } else {
            /* Literal header not indexed */
            flags = WXHTTP_HDR_TRANSIENT;
            mask = 0x0f;
        }

        /* All cases have the primary idx of interest */
        l = WXHTTP_HDecodeInt(ptr, len, mask, &idx);
        if (l < 0) return l;

        /* Immediate handling for fully indexed/resize conditions */
        if (isFullyIndexed) {
            if (idx == 0) return WXNRC_DATA_ERROR;
            if (idx < STATIC_HEADER_COUNT) {
                hdr = WXHTTP_CloneHeader(NULL, &(wxhttpStaticHeaders[idx]));
            } else {
                idx -= STATIC_HEADER_COUNT;
                if (idx >= dynTable->entryCount) return WXNRC_DATA_ERROR;
                idx = dynTable->entryCount - idx - 1;
                hdr = WXHTTP_CloneHeader(NULL, dynTable->entries[idx]);
            }
            if (hdr == NULL) return WXNRC_MEM_ERROR;
            hdr->next = *hdrRef;
            *hdrRef = hdr;

            ptr += l; len -= l;
            buff->offset = ptr - buff->buffer;
            continue;
        }
        if (isResize) {
            /* TODO - validate absolute limit */
            _resizeDynamicHeaders(dynTable, idx);
            ptr += l; len -= l;
            buff->offset = ptr - buff->buffer;
            continue;
        }

        /* Figure out the name information */
        ptr += l; len -= l;
        if (idx != 0) {
            /* References into table, grab appropriate references */
            if (idx < STATIC_HEADER_COUNT) {
                hdr = &(wxhttpStaticHeaders[idx]);
                nm = hdr->name;
                nmLen = hdr->nameLen;
                flags |= WXHTTP_HDR_STATIC_NAME;
            } else {
                idx -= STATIC_HEADER_COUNT;
                if (idx >= dynTable->entryCount) return WXNRC_DATA_ERROR;
                idx = dynTable->entryCount - idx - 1;
                hdr = dynTable->entries[idx];
                nm = hdr->name;
                nmLen = hdr->nameLen;
            }
        } else {
            l = _decodeStringLiteral(ptr, len, &nm, &nmLen);
            if (l < 0) return l;
            flags |= WXHTTP_HDR_PREALLOC_NAME;
            ptr += l; len -= l;
        }

        /* At this juncture, all values are encoded */
        l = _decodeStringLiteral(ptr, len, &val, &valLen);
        if (l < 0) {
            if ((flags & WXHTTP_HDR_PREALLOC_NAME) != 0) WXFree(nm);
            return l;
        }
        flags |= WXHTTP_HDR_PREALLOC_VALUE;
        ptr += l; len -= l;

        /* Push the allocated header instance */
        hdr = _allocInitHeader(NULL, nm, nmLen, val, valLen, flags);
        if (hdr == NULL) {
            if ((flags & WXHTTP_HDR_PREALLOC_NAME) != 0) WXFree(nm);
            WXFree(val);
            return WXNRC_MEM_ERROR;
        }
        hdr->next = *hdrRef;
        *hdrRef = hdr;

        /* Update the dynamic table for indexable entries */
        rc = _pushDynamicHeader(dynTable, hdr);
        if (rc != WXNRC_OK) return rc;

        /* Mark consumed buffer position for continuation */
        buff->offset = ptr - buff->buffer;
    }

    return WXNRC_OK;
}

/* Cleanup for the tables */
void WXHTTP_DestroyDynamicHeaders(WXHTTPDynamicHeaders *dynTable) {
    uint32_t idx;

    /* Just flush the header records */
    for (idx = 0; idx < dynTable->entryCount; idx++) {
        WXHTTP_DestroyHeader(dynTable->entries[idx], TRUE);
    }
    WXFree(dynTable->entries);

    /* Clean up for testing reuse */
    dynTable->entries = NULL;
    dynTable->tableSize = dynTable->entryCount = dynTable->entryAllocCount = 0;
}
